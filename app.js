
const calculator = {
    add: function(num1,num2) {
        console.log(num1 + num2);
    },
    minus: function(num1,num2) {
        console.log(num1 - num2);
    },
    multi: function(num1,num2) {
        console.log(num1*num2);
    },
    devide: function(num1,num2) {
        console.log(num1/num2);
    },
    powerof: function(num1,num2) {
        console.log(num1**num2);
    }
}




calculator.add(1,2);
